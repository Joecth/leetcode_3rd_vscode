/**
 * Definition for singly-linked list.
 * function ListNode(val) {
 *     this.val = val;
 *     this.next = null;
 * }
 */
/**
 * @param {ListNode} head
 * @return {ListNode}
 */
var deleteDuplicates = function(head) {
    if (head == null){return head;}

    let j = cur = head;
    while (cur){
        if (cur.val != j.val){
            j = j.next;
        }
        j.val = cur.val;
        cur = cur.next;
    }
    j.next = null;
    return head;
    
};
