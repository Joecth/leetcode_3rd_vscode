/**
 * Definition for a binary tree node.
 * function TreeNode(val) {
 *     this.val = val;
 *     this.left = this.right = null;
 * }
 */
/**
 * @param {TreeNode} root
 * @return {void} Do not return anything, modify root in-place instead.
 */
var flatten = function(root) {
    prev = null;
    helper(root);
    function helper(root){
        if (null == root) return;
        helper(root.right);
        helper(root.left);

        root.right = prev;
        root.left = null;
        prev = root;
    }
};
