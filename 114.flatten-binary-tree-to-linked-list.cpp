/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode(int x) : val(x), left(NULL), right(NULL) {}
 * };
 */
class Solution {
private:
    TreeNode* prev = nullptr;
public:
    void flatten(TreeNode* root) {
        helper(root);
    }

    void helper(TreeNode* root){
        if (nullptr == root) return;
        helper(root->right);
        helper(root->left);
        root->right = prev;
        root->left = nullptr;
        prev = root;
    }
};
